# Proyecto 8: Dibujo con Tortuga
# Descripción: Usar la librería turtle para dibujar formas y patrones.

import turtle

# Inicializar la pantalla y la tortuga
screen = turtle.Screen()
t = turtle.Turtle()

# Dibujar un cuadrado
for _ in range(4):
    t.forward(100)
    t.right(90)

turtle.done()



