# Ejemplos de operaciones matemáticas
x = 10
y = 3

suma = x + y  # Suma: 13
resta = x - y  # Resta: 7
multiplicacion = x * y  # Multiplicación: 30
division = x / y  # División: 3.3333333333333335
modulo = x % y  # Módulo (resto de la división): 1
exponente = x ** y  # Exponente (x elevado a la y): 1000
division_entera = x // y  # División entera: 3


print(suma)
print(resta)
print(multiplicacion)
print(division)
print(modulo)
print(exponente)
print(division_entera)

print(type(suma))
print(type(resta))
print(type(multiplicacion))
print(type(division))
print(type(modulo))
print(type(exponente))
print(type(division_entera))






