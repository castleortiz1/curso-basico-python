# Datos de ejemplo
edades = [12, 14, 15, 13, 12, 14, 15, 16]
nombres = ["Ana", "Luis", "Carlos", "Marta", "Jose", "Lucia", "Pedro", "Elena"]

# Calcular la edad promedio
promedio = sum(edades) / len(edades)
print(f"Edad promedio: {promedio}")

# Encontrar la edad mínima y máxima
edad_min = min(edades)
edad_max = max(edades)
print(f"Edad mínima: {edad_min}, Edad máxima: {edad_max}")
